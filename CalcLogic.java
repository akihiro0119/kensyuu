public class CalcLogic {
  public static int plus(int a, int b){
    return(a + b);
  }
  public static int minus(int a, int b){
    return(a - b);
  }

  public static int call(int a, int b){
    return(a * b);
  }

  public static int waru(int a, int b){
    return(a / b);
  }

  public static int amari(int a, int b){
    return(a % b);
  }
}
